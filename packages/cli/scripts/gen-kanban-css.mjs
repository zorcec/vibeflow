#!/usr/bin/env node
/**
 * Generates src/server/kanban-css.gen.ts from the Tailwind CSS input.
 * Run automatically via the "prebuild" npm script.
 *
 * Single source of truth: src/client/kanban/kanban.css
 * Uses tailwindcss CLI to scan TSX files for class names and emit production CSS.
 */
import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const inputCss = resolve(root, 'src/client/kanban/kanban.css');
const outputCss = resolve(root, 'dist/client/kanban.css');
const outputTs = resolve(root, 'src/server/kanban-css.gen.ts');

execSync(
  `npx tailwindcss -c tailwind.config.cjs -i "${inputCss}" -o "${outputCss}" --minify`,
  { cwd: root, stdio: 'inherit' },
);

const css = readFileSync(outputCss, 'utf-8');
const esc = (s) => s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

const output = `// AUTO-GENERATED by scripts/gen-kanban-css.mjs — DO NOT EDIT.
// Edit src/client/kanban/kanban.css, then run 'npm run build'.

/** Production Tailwind CSS for the Kanban React app. Inlined into the HTML shell. */
export const KANBAN_CSS = \`${esc(css)}\`;
`;

writeFileSync(outputTs, output, 'utf-8');
console.log('[gen-kanban-css] Generated src/server/kanban-css.gen.ts');
